console.log("=== How to Deploy Your Portfolio to Vercel for Free ===\n")

console.log("Step 1: Prepare Your Repository")
console.log("- Push your code to a GitHub repository")
console.log("- Make sure your project has a proper structure with:")
console.log("  * package.json with dependencies")
console.log("  * next.config.js (if using Next.js)")
console.log("  * All necessary source files\n")

console.log("Step 2: Sign Up for Vercel")
console.log("- Go to https://vercel.com/")
console.log("- Sign up with your GitHub account")
console.log("- This allows Vercel to access your repositories\n")

console.log("Step 3: Import Your Project")
console.log("- From the Vercel dashboard, click 'Add New...' → 'Project'")
console.log("- Select the GitHub repository containing your portfolio")
console.log("- Vercel will automatically detect Next.js and configure the build settings\n")

console.log("Step 4: Configure Project Settings")
console.log("- Project Name: Choose a name for your project (will be part of the URL)")
console.log("- Framework Preset: Should auto-detect Next.js")
console.log("- Root Directory: Leave as '/' if your project is at the root of the repository")
console.log("- Build Command: Should be auto-filled (typically 'next build')")
console.log("- Output Directory: Should be auto-filled (typically '.next')\n")

console.log("Step 5: Deploy")
console.log("- Click 'Deploy'")
console.log("- Vercel will build and deploy your project")
console.log("- You'll get a URL like: https://your-project.vercel.app\n")

console.log("Step 6: Custom Domain (Optional)")
console.log("- In your project dashboard, go to 'Settings' → 'Domains'")
console.log("- Add your custom domain if you have one")
console.log("- Follow the instructions to configure DNS settings\n")

